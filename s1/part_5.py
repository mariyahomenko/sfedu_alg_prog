# Перевод между непрямо связанными системами счисления

# N_p → N_q          p != 10   q != 10
# N_p → N_10 → N_q
# Для неродственных систем используем промежуточный перевод через десятичную
# N_3 → N_10 → N_7
# 1020_3 → N_10
# 1×3³ + 0×3² + 2×3¹ + 0×3⁰ = 1×27 + 0×9 + 2×3 + 0×1 = 27 + 0 + 6 + 0 = 33
# 33_10 → N_7
# 33 ÷ 7 = 4 (остаток 5), 4  ÷ 7 = 0 (остаток 4)
# Читаем остатки снизу вверх: 45_7

# Для систем с основаниями вида 2ⁿ:
# N_(2**p) → N_(2**q)
# N_(2**p) → N_2 → N_(2**q)
# 13022_4 → N_2 (группировка по 2 бита)
# Четверичные цифры → двоичные пары (дуады)
# 1 = 01₂
# 3 = 11₂  
# 0 = 00₂
# 2 = 10₂
# 2 = 10₂
# Объединяем: 01 11 00 10 10 = 0111001010₂
# 0111001010_2 → N_8 (группировка по 3 бита)
# Двоичное число → восьмеричные триады
# 0 111 001 010
# 0 = 0₈
# 111 = 7₈
# 001 = 1₈  
# 010 = 2₈
# Результат: 712₈

# Для родственных систем (2ⁿ) используем перевод через двоичную систему
# Дуады, триады, тетрады - группы бит для быстрого перевода
# Ведущие нули важны для правильной группировки
