# Перевод чисел между системами счисления

# Развернутая форма - представление числа как суммы произведений цифр на степени основания
# Индексы показывают позиции цифр (разряды)
# Для шестнадцатеричной системы: a=10, b=11, c=12, d=13, e=14, f=15

# Синтаксис: int(string, base)
# string - строка с числом
# base - основание системы счисления (от 2 до 36)
# По умолчанию base=10, 0 интерпретируется как десятичная система
# Максимальное основание - 36, т.к. цифры 0-9 + буквы a-z = 36 символов

# Префиксы прямой записи:
# 0b или 0B - двоичная система
# 0o или 0O - восьмеричная система
# 0x или 0X - шестнадцатеричная система

# 10110₂ = 1×2⁴ + 0×2³ + 1×2² + 1×2¹ + 0×2⁰
result = 1*(2**4) + 0*(2**3) + 1*(2**2) + 1*(2**1) + 0*(2**0)
print(result)  # 16 + 0 + 4 + 2 + 0 = 22

# Разбираем по цифрам (справа налево):
# Позиции: 4 3 2 1 0
# Цифры:   1 0 1 1 0

# Цифра 0 (позиция 0): 0 × 2⁰ = 0 × 1 = 0
# Цифра 1 (позиция 1): 1 × 2¹ = 1 × 2 = 2  
# Цифра 1 (позиция 2): 1 × 2² = 1 × 4 = 4
# Цифра 0 (позиция 3): 0 × 2³ = 0 × 8 = 0
# Цифра 1 (позиция 4): 1 × 2⁴ = 1 × 16 = 16

binary = "10110"
decimal = 0
for i, digit in enumerate(reversed(binary)):
    decimal += int(digit) * (2 ** i)
print(f"Результат цикла: {decimal}")

# ad₁₆ = a×16¹ + d×16⁰ = 10×16 + 13×1 (a=10, b=11, c=12, d=13)
result = 10*(16**1) + 13*(16**0)
print(result)  # 160 + 13 = 173
