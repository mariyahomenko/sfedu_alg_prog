# Целочисленное деление и операция divmod

# Целочисленное деление: // (div)
# Остаток от целочисленного деления: % (mod)
# divmod(m, n): кортеж (m//n, m%n)

m = 78
n = 5
print(m // n)           # 15
print(m % n)            # 3
print(divmod(m, n))     # (15, 3)

print(divmod(10, 3))    # (3, 1)
# Оба числа положительные
# 10 // 3 = 3  Сколько целых троек входит в 10
# 10 % 3 = 1  Остаток от деления

print(divmod(-10, 3))   # (-4, 2)
# Отрицательное делимое
# -10 // 3 = -4  Округление в меньшую сторону
# -10 % 3 = 2  Остаток всегда неотрицательный для положительного делителя

print(divmod(10, -3))   # (-4, -2) 
# Отрицательный делитель
# 10 // -3 = -4  Округление в меньшую сторону  
# 10 % -3 = -2  Знак остатка совпадает со знаком делителя

print(divmod(-10, -3))  # (3, -1)
# Оба числа отрицательные
# -10 // -3 = 3  Деление отрицательных дает положительный результат
# -10 % -3 = -1  Остаток имеет отрицательный знак делителя

# Целочисленное деление // всегда округляет в сторону -∞
# Для положительных чисел - округление вниз
print(7 // 2)   # 3 (не 3.5)
# Для отрицательных чисел - тоже округление вниз  
print(-7 // 2)  # -4 (не -3)

