# Перевод из десятичной системы в другие системы счисления

#Алгоритм перевода методом деления:
# Делим число на основание новой системы
# Записываем остаток
# Целую часть снова делим на основание
# Повторяем до тех пор, пока целая часть не станет 0
# Остатки читаем снизу вверх

# Ручной расчет:
# 35 ÷ 2 = 17 (остаток 1)
# 17 ÷ 2 = 8  (остаток 1)
# 8  ÷ 2 = 4  (остаток 0)
# 4  ÷ 2 = 2  (остаток 0)
# 2  ÷ 2 = 1  (остаток 0)
# 1  ÷ 2 = 0  (остаток 1)
# Читаем остатки снизу вверх: 100011₂

# Перевод 35₁₀ → N₈
# 35 ÷ 8 = 4 (остаток 3)
# 4  ÷ 8 = 0 (остаток 4)
# Читаем остатки снизу вверх: 43₈

x = 35
# Преобразование в разные системы счисления
x2 = bin(x)[2:]    # '100011' - убираем префикс '0b'
x8 = oct(x)[2:]    # '43'     - убираем префикс '0o'  
x10 = str(x)       # '35'
x16 = hex(x)[2:]   # '23'     - убираем префикс '0x'
print(f'{x} = {x2}_2')
print(f'{x} = {x8}_8')
print(f'{x} = {x10}_10')
print(f'{x} = {x16}_16')
# 35 = 100011_2
# 35 = 43_8
# 35 = 35_10
# 35 = 23_16

