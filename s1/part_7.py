# Работа с цифрами целого десятичного числа

# Позиционная система: каждая цифра имеет свой "вес" в зависимости от позиции
# Формулы для извлечения цифр десятичного
# n0 = n % 10               цифра единиц
# n1 = n // 10 % 10         цифра десятков
# n2 = n // 100 % 10        цифра сотен
# n3 = n // 1000            цифра тысяч
# Общая формула для k-й цифры
# n_k = n // 10**k % 10

n = 6732
# Единицы (k=0)
n0 = 6732 % 10           # = 2
# 6732 ÷ 10 = 673.2 → остаток 2
# Десятки (k=1)  
n1 = 6732 // 10 % 10     # = 673 % 10 = 3
# 6732 ÷ 10 = 673 (целочисленно)
# 673 % 10 = 3
# Сотни (k=2)
n2 = 6732 // 100 % 10    # = 67 % 10 = 7
# 6732 ÷ 100 = 67
# 67 % 10 = 7
# Тысячи (k=3)
n3 = 6732 // 1000        # = 6
# 6732 ÷ 1000 = 6

n = 13                   # 1101₂
n0 = 13 // 2**0 % 2      # 13 // 1 % 2 = 13 % 2 = 1
n1 = 13 // 2**1 % 2    # 13 // 2 % 2 = 6 % 2 = 0  
n2 = 13 // 2**2 % 2    # 13 // 4 % 2 = 3 % 2 = 1
n3 = 13 // 2**3 % 2    # 13 // 8 % 2 = 1 % 2 = 1
# Результат: 1, 0, 1, 1 → 1101₂

# Пример: В трехзначном числе поменять местами первую и последнюю цифру
n = int(input('n = '))
n0 = n % 10
n1 = n // 10 % 10 
n2 = n // 100
n = n0*100 + n1*10 + n2
print(n)

n = 345
# Извлекаем цифры
n0 = 345 % 10                   # = 5 (единицы)
n1 = 345 // 10 % 10             # = 34 % 10 = 4 (десятки)
n2 = 345 // 100                 # = 3 (сотни)
# Переставляем цифры: n2 n1 n0 → n0 n1 n2
result = n0*100 + n1*10 + n2    # = 5*100 + 4*10 + 3 = 543
print(result)                   # 543

# Операция % 10 извлекает последнюю цифру
# Операция // 10 удаляет последнюю цифру
# Обобщается на любые системы счисления заменой основания 10
